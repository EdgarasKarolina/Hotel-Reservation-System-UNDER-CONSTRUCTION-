public class User {

    int userId;
    String userName;
    String password;
    String name;
    String surname;

    public User( int userId, String userName, String password, String name, String surname) {
        this.userId = userId;
        this.userName = userName;
        this.password = password;
        this.name = name;
        this.surname = surname;
    }


}
